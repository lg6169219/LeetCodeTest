/**
反转一个单链表。

示例:

输入: 1->2->3->4->5->NULL
输出: 5->4->3->2->1->NULL
进阶:
你可以迭代或递归地反转链表。你能否用两种方法解决这道题？
*/

#include <iostream>

struct ListNode {
	int val;
	ListNode *next;
	ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
public:
	// 迭代
	ListNode* reverseList(ListNode* head) {
		if (head == NULL || head->next == NULL)
		{
			return head;
		}

		ListNode* cur = head->next;
		ListNode* movNode = NULL;
		// 把头结点取出来next指向NULL
		head->next = NULL;

		while (cur != NULL)
		{
			movNode = cur;
			cur = cur->next;
			movNode->next = head;
			head = movNode;
		}
		return head;
	}

};